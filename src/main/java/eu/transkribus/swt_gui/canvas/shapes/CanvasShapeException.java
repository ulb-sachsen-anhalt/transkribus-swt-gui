package eu.transkribus.swt_gui.canvas.shapes;

public class CanvasShapeException extends RuntimeException {
	private static final long serialVersionUID = -6949472816633749246L;

	public CanvasShapeException() {
		super();
	}

	public CanvasShapeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CanvasShapeException(String message, Throwable cause) {
		super(message, cause);
	}

	public CanvasShapeException(String message) {
		super(message);
	}

	public CanvasShapeException(Throwable cause) {
		super(cause);
	}
	
	

}
